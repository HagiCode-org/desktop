import fs from 'node:fs/promises';
import path from 'node:path';
import { readRuntimeManifestRoot } from './runtime-manifest-store.js';

const PROJECT_ROOT = process.cwd();
const MANIFEST_PATH = path.join(PROJECT_ROOT, 'resources', 'manifest.yml');
const OUTPUT_PATH = path.join(PROJECT_ROOT, 'src', 'shared', 'generated', 'managed-package-manifest.ts');

function isRecord(value) {
  return value !== null && typeof value === 'object' && !Array.isArray(value);
}

function validateManifestPackages(rawPackages) {
  if (!isRecord(rawPackages)) {
    throw new Error('resources/manifest.yml npmSync.packages must be a mapping.');
  }

  const entries = Object.entries(rawPackages)
    .sort(([left], [right]) => left.localeCompare(right))
    .map(([packageName, rawEntry]) => {
      if (!isRecord(rawEntry)) {
        throw new Error(`npmSync.packages.${packageName} must be an object.`);
      }

      const version = typeof rawEntry.version === 'string' ? rawEntry.version.trim() : '';
      const targetValue = typeof rawEntry.target === 'string' ? rawEntry.target.trim() : '';
      const installArgs = Array.isArray(rawEntry.installArgs)
        ? rawEntry.installArgs
          .filter((value) => typeof value === 'string')
          .map((value) => value.trim())
          .filter(Boolean)
        : undefined;
      if (!version) {
        throw new Error(`npmSync.packages.${packageName}.version must be a non-empty string.`);
      }

      return {
        packageName,
        version,
        target: targetValue || undefined,
        installArgs: installArgs && installArgs.length > 0 ? installArgs : undefined,
      };
    });

  return entries;
}

function renderEntry(entry) {
  const lines = [
    `  ${JSON.stringify(entry.packageName)}: {`,
    `    version: ${JSON.stringify(entry.version)},`,
  ];

  if (entry.target) {
    lines.push(`    target: ${JSON.stringify(entry.target)},`);
  }

  if (entry.installArgs) {
    lines.push(`    installArgs: ${JSON.stringify(entry.installArgs)},`);
  }

  lines.push('  },');
  return lines.join('\n');
}

function renderOutput(entries) {
  return `// Auto-generated by scripts/generate-managed-npm-packages.js
// Source: resources/manifest.yml (npmSync.packages)

export interface RuntimeManagedPackageManifestEntry {
  version: string;
  target?: string;
  installArgs?: string[];
}

export const runtimeManagedPackageManifestPackages: Readonly<Record<string, RuntimeManagedPackageManifestEntry>> = {
${entries.map((entry) => renderEntry(entry)).join('\n')}
} as const;
`;
}

async function main() {
  const manifest = readRuntimeManifestRoot({ manifestPath: MANIFEST_PATH });
  const rawPackages = isRecord(manifest.npmSync) ? manifest.npmSync.packages : undefined;
  const entries = validateManifestPackages(rawPackages);
  const output = renderOutput(entries);
  await fs.mkdir(path.dirname(OUTPUT_PATH), { recursive: true });
  await fs.writeFile(OUTPUT_PATH, output, 'utf8');
  console.log(`[managed-npm-packages] generated ${path.relative(PROJECT_ROOT, OUTPUT_PATH)} from resources/manifest.yml`);
}

main().catch((error) => {
  console.error(`[managed-npm-packages] ${error instanceof Error ? error.message : String(error)}`);
  process.exitCode = 1;
});
